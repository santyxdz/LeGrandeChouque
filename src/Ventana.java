    public boolean isPlaca(String placa){
        boolean ret=true;

        //Saber si tiene 6 digitos
        if(placa.length()==6){
            //Si tiene 6 digitos
            //Saber si los 3 primeros son letras (Suponemos que estan en minusculas)
            for(int i=0;i<3;i++){
                //Si el caracter no es una Letra Mayuscula false
                if(((int)placa.charAt(i))<65||((int)placa.charAt(i))>90){
                    ret=false;
                }
            }
            //Saber si los 3 Ultimos son numeros
            try {
                Integer.parseInt(placa.substring(3,6));
            } catch (Exception e) {
                ret=false;
            }
        }
        else{
        ret = false;
        }
        return ret;
    }
    public boolean isID(String id){
        try {
            Integer.parseInt(id);
        } catch (Exception e) {
            return false;
        }
        return true;
    }
    public boolean isHH(String hh){
        boolean ret = true;
        try {
            int num = Integer.parseInt(hh);
            if(num<0||num>23){
                ret=false;
            }
        } catch (Exception e) {
            ret=false;
        }
        return ret;
    }
    public boolean isMM(String mm){
        boolean ret = true;
        try {
            int num = Integer.parseInt(mm);
            if(num<0||num>59){
                ret=false;
            }
        } catch (Exception e) {
            ret=false;
        }
        return ret;
    }
    public String proxHora(){
        String ret = "";
        if(torre.proxSalir()==null){
            ret = "HH:MM";
        }
        else{
            ret = torre.proxSalir().getHoraRetirada().getHora()+":"+torre.proxSalir().getHoraRetirada().getMin();
        }
        return ret;
    }
