                    double cobro = 0.0;
                    int nivel = (int)ipt_retiro.getText().charAt(0)-65; //Saber Nivel
                    int entrada = (int)ipt_retiro.getText().charAt(ipt_retiro.getText().length()-1)-48; //Saber Entrada
