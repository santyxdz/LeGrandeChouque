    private void actualizar(){
        //Horas Panel Derecho
        lbl_horas.setText(Integer.toString(torre.Clock().getHoraActual().getHora()));
        lbl_minutos.setText(Integer.toString(torre.Clock().getHoraActual().getMin()));
        //Horas Registro
        lbl_hh.setText(Integer.toString(torre.Clock().getHoraActual().getHora()));
        lbl_mm.setText(Integer.toString(torre.Clock().getHoraActual().getMin()));
        //Luagres Disponibles
        lbl_num_disp.setText(Integer.toString(6340-torre.nVehiculos()));
        //Lugares en Sotano
        lbl_sotano_size.setText(Integer.toString(torre.inSotano()));
        //Proximo Vehiculo en Salir
        lbl_prox_placa.setText(proxPlaca());
        lbl_prox_hora.setText(proxHora());
        //estado
        if (torre.status()) {
            lbl_stat.setForeground(new java.awt.Color(91, 165, 37));
            lbl_stat.setText("ACTIVO");
        }
        else {
            lbl_stat.setForeground(new java.awt.Color(221,0,23));
            lbl_stat.setText("INACTIVO");
        }
    }
